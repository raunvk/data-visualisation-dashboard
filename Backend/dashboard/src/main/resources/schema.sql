CREATE TABLE IF NOT EXISTS data(
	entry_id VARCHAR(100) PRIMARY KEY,
	end_year VARCHAR(100),
	citylng VARCHAR(100),
	citylat VARCHAR(100),
	intensity VARCHAR(100),
	sector VARCHAR(100),
	topic VARCHAR(100),
	insight VARCHAR(1000),
	swot VARCHAR(100),
	url VARCHAR(1000),
	region VARCHAR(100),
	start_year VARCHAR(100),
	impact VARCHAR(100),
	added VARCHAR(100),
	published VARCHAR(100),
	city VARCHAR(100),
	country VARCHAR(100),
	relevance VARCHAR(100),
	pestle VARCHAR(100),
	source VARCHAR(100),
	title VARCHAR(1000),
	likelihood VARCHAR(100)
);
